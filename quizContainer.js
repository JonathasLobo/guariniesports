let shuffledQuestions = [];
let currentIndex = 0;
let score = 0;

const gameDiv = document.getElementById("game");

document.getElementById("startBtn").addEventListener("click", startGame);

function startGame() {
  score = 0;
  currentIndex = 0;
  shuffledQuestions = shuffleArray([...quizQuestions]); // embaralha
  showQuestion();
}

function showQuestion() {
  const currentQuestion = shuffledQuestions[currentIndex];

  const answersHTML = Object.keys(currentQuestion.answers).map(letter => 
    `<label>
      <input type="radio" name="answer" value="${letter}">
      ${letter}: ${currentQuestion.answers[letter]}
    </label>`
  ).join("");

  gameDiv.innerHTML = `
    <div class="question">${currentQuestion.question}</div>
    <div class="answers">${answersHTML}</div>
    <button id="submit">Responder</button>
  `;

  document.getElementById("submit").addEventListener("click", checkAnswer);
}

function checkAnswer() {
  const currentQuestion = shuffledQuestions[currentIndex];
  const selected = document.querySelector("input[name=answer]:checked");

  if (!selected) return; // se n√£o marcou nada

  if (selected.value === currentQuestion.correctAnswer) {
    score++;
    currentIndex++;

    if (currentIndex < shuffledQuestions.length) {
      showQuestion();
    } else {
      endGame(true);
    }
  } else {
    endGame(false);
  }
}

